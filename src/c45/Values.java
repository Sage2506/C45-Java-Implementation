package c45;

public class Values {
 String name;
 String clase;
 // constructor del Valor nombre y clase
 public Values(String name, String clase) {
	 this.name = name;
	 this.clase = clase;
 }
 // Obtencion del valor del Valor
 public String name() {
	 return this.name;
 }
 // Obtencion de la clase del Valor
 public String clase() {
	 return this.clase;
 }

}
